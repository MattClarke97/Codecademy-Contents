var moonPhase = "full";

switch (moonPhase) {
  case "full":
    console.log("Howwwwlll!!");
    break;
  case "mostly full":
    console.log("Arms and legs are getting hairier");
    break;
  case "mostly new":
    console.log("Back on two feet");
  default:
    console.log("invalid moon phase");
    break;
}
